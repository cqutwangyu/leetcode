public class FindTheHighestAltitude {
    class Solution {
        public int largestAltitude(int[] gain) {
            //æ‰¾æœ€å¤§å€¼
            int max = Math.max(0, gain[0]);
            //ä¸‹æ ‡1å¼€å§‹æ±‚å‰ç¼€å’Œ
            for (int i = 1; i < gain.length; i++) {
                gain[i] += gain[i - 1];
                max = Math.max(gain[i], max);
            }
            return max;
        }
    }
}
