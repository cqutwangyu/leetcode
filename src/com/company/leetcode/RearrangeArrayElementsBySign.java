public class RearrangeArrayElementsBySign {
    class Solution {
        public int[] rearrangeArray(int[] nums) {
            int[] ints = new int[nums.length];
            for (int i = 0, q = 0, p = 0; i < nums.length; i++) {
                while (nums[q] < 0) {
                    q++;
                }
                while (nums[p] > 0) {
                    p++;
                }
                ints[i++] = nums[q++];
                ints[i] = nums[p++];
            }
            return ints;
        }
    }
}
