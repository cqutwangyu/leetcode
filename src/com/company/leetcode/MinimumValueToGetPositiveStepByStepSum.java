public class MinimumValueToGetPositiveStepByStepSum {
    class Solution {
        public int minStartValue(int[] nums) {
            //æ‰¾æœ€å°å€¼
            int min = Math.min(1, nums[0]);
            //ä¸‹æ ‡1å¼€å§‹æ±‚å‰ç¼€å’Œ
            for (int i = 1; i < nums.length; i++) {
                nums[i] += nums[i - 1];
                min = Math.min(min, nums[i]);
            }
            return min < 0 ? Math.abs(min) + 1 : 1;
        }
    }
}
